<?php

namespace Login\LoginBundle\Entity;

/**
 * Transaction
 */
class Transaction
{
    /**
     * @var string
     */
    private $tmId;

    /**
     * @var string
     */
    private $trType;

    /**
     * @var string
     */
    private $trAmount;

    /**
     * @var string
     */
    private $trDate;

    /**
     * @var integer
     */
    private $trId;


    /**
     * Set tmId
     *
     * @param string $tmId
     *
     * @return Transaction
     */
    public function setTmId($tmId)
    {
        $this->tmId = $tmId;

        return $this;
    }

    /**
     * Get tmId
     *
     * @return string
     */
    public function getTmId()
    {
        return $this->tmId;
    }

    /**
     * Set trType
     *
     * @param string $trType
     *
     * @return Transaction
     */
    public function setTrType($trType)
    {
        $this->trType = $trType;

        return $this;
    }

    /**
     * Get trType
     *
     * @return string
     */
    public function getTrType()
    {
        return $this->trType;
    }

    /**
     * Set trAmount
     *
     * @param string $trAmount
     *
     * @return Transaction
     */
    public function setTrAmount($trAmount)
    {
        $this->trAmount = $trAmount;

        return $this;
    }

    /**
     * Get trAmount
     *
     * @return string
     */
    public function getTrAmount()
    {
        return $this->trAmount;
    }

    /**
     * Set trDate
     *
     * @param string $trDate
     *
     * @return Transaction
     */
    public function setTrDate($trDate)
    {
        $this->trDate = $trDate;

        return $this;
    }

    /**
     * Get trDate
     *
     * @return string
     */
    public function getTrDate()
    {
        return $this->trDate;
    }

    /**
     * Get trId
     *
     * @return integer
     */
    public function getTrId()
    {
        return $this->trId;
    }
}
